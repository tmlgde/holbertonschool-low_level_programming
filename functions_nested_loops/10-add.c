/**
 * add - Adds two integers.
 * @a: The first integer to be added.
 * @b: The second integer to be added.
 *
 * Description: This function takes two integers as input, adds them together,
 * and returns the sum. The result is returned as an integer.
 *
 * Return: The sum of the two integers (a + b).
 */
int add(int a, int b)
{
	int sum = a + b;

	return (sum);
}
