#include "main.h"
#include <stdio.h>

/**
 * mul - Multiplie deux entiers.
 * @a: Le premier entier à multiplier.
 * @b: Le second entier à multiplier.
 *
 * Description: Cette fonction prend deux entiers en paramètres et renvoie
 * leur produit.
 *
 * Return: Le résultat de la multiplication de a et b.
 */

int mul(int a, int b)
{
		return (a * b);
}
